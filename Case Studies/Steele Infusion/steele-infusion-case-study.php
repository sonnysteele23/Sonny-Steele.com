    <script>

        // Mobile menu toggle
        const mobileMenuToggle = document.getElementById('mobileMenuToggle');
        const navLinks = document.querySelector('.nav-links');

        if (mobileMenuToggle && navLinks) {
            mobileMenuToggle.addEventListener('click', () => {
                navLinks.classList.toggle('active');
                mobileMenuToggle.classList.toggle('active');
            });

            // Close mobile menu when clicking outside
            document.addEventListener('click', (e) => {
                if (!navLinks.contains(e.target) && !mobileMenuToggle.contains(e.target)) {
                    navLinks.classList.remove('active');
                    mobileMenuToggle.classList.remove('active');
                }
            });

            // Close mobile menu when clicking on links
            const mobileNavLinks = navLinks.querySelectorAll('a');
            mobileNavLinks.forEach(link => {
                link.addEventListener('click', () => {
                    navLinks.classList.remove('active');
                    mobileMenuToggle.classList.remove('active');
                });
            });
        }

        // Resume link functionality (redirect to main site)
        const resumeLinkHeader = document.getElementById('resumeLinkHeader');
        if (resumeLinkHeader) {
            resumeLinkHeader.addEventListener('click', () => {
                window.open('https://sonny-steele.com', '_blank');
            });
        }

        // Modal functionality
        const modal = document.getElementById('imageModal');
        const modalImage = document.getElementById('modalImage');
        const modalClose = document.getElementById('modalClose');
        const modalPrev = document.getElementById('modalPrev');
        const modalNext = document.getElementById('modalNext');

        // Business card images array
        const businessCardImages = [
            'images/steele-infusion-business-card-front.png',
            'images/steele-infusion-business-card-back.png'
        ];
        let currentBusinessCardIndex = 0;

        // Open business card modal with navigation
        function openBusinessCardModal(startIndex = 0) {
            currentBusinessCardIndex = startIndex;
            modal.classList.add('active');
            modalImage.src = businessCardImages[currentBusinessCardIndex];
            modalImage.alt = currentBusinessCardIndex === 0 ? 'Business Card Front' : 'Business Card Back';
            document.body.style.overflow = 'hidden';
            modalPrev.style.display = 'block';
            modalNext.style.display = 'block';
        }

        // Regular modal function (for single images)
        function openModal(imageSrc, imageAlt = '') {
            modal.classList.add('active');
            modalImage.src = imageSrc;
            modalImage.alt = imageAlt;
            document.body.style.overflow = 'hidden';
            modalPrev.style.display = 'none';
            modalNext.style.display = 'none';
            
            // Apply branding class for brand design images
            if (imageSrc.includes('steeleinfusionWebsite.png')) {
                modalImage.classList.add('branding');
            } else {
                modalImage.classList.remove('branding');
            }
        }

        // Navigate business cards
        function nextBusinessCard() {
            currentBusinessCardIndex = (currentBusinessCardIndex + 1) % businessCardImages.length;
            modalImage.src = businessCardImages[currentBusinessCardIndex];
            modalImage.alt = currentBusinessCardIndex === 0 ? 'Business Card Front' : 'Business Card Back';
        }

        function prevBusinessCard() {
            currentBusinessCardIndex = (currentBusinessCardIndex - 1 + businessCardImages.length) % businessCardImages.length;
            modalImage.src = businessCardImages[currentBusinessCardIndex];
            modalImage.alt = currentBusinessCardIndex === 0 ? 'Business Card Front' : 'Business Card Back';
        }

        function addImageClickListeners() {
            const images = document.querySelectorAll('.highlight-image img, img[onclick]');
            images.forEach(img => {
                if (!img.onclick) {
                    img.addEventListener('click', (e) => {
                        e.preventDefault();
                        openModal(img.src, img.alt);
                    });
                }
                img.style.cursor = 'pointer';
            });
        }

        // Close modal
        function closeModal() {
            modal.classList.remove('active');
            document.body.style.overflow = 'auto';
        }

        modalClose.addEventListener('click', closeModal);
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });

        // Navigation arrow event listeners
        modalPrev.addEventListener('click', prevBusinessCard);
        modalNext.addEventListener('click', nextBusinessCard);

        // Close modal with Escape key and add arrow key navigation
        document.addEventListener('keydown', (e) => {
            if (modal.classList.contains('active')) {
                if (e.key === 'Escape') {
                    closeModal();
                } else if (modalPrev.style.display === 'block') {
                    // Only allow arrow navigation if we're in business card modal
                    if (e.key === 'ArrowLeft') {
                        prevBusinessCard();
                    } else if (e.key === 'ArrowRight') {
                        nextBusinessCard();
                    }
                }
            }
        });

        // Initialize modal listeners when page loads
        addImageClickListeners();
    </script>
